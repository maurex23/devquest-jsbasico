function soma(num1, num2) {
    return num1 + num2
}
function subtracao(num1, num2) {
    return num1 - num2
}
function multiplicacao(num1, num2) {
    return num1 * num2
}
function divisao(num1, num2) {
    return num1 / num2
}

a = soma(5, 5)
b = subtracao(5, 5) 
c = multiplicacao(5, 5)
d = divisao(1, 2)

console.log([a, b, c, d])