function soma(num1, num2) {
    return num1 + num2
}
function subtracao(num1, num2) {
    return num1 - num2
}
function multiplicacao(num1, num2) {
    return num1 * num2
}
function divisao(num1, num2) {
    return num1 / num2
}

const a = soma(5, 5)
const b = subtracao(5, 5) 
const c = multiplicacao(5, 5)
const d = divisao(1, 2)

